n = int(input("Сколько голов? "))

if n <= 0 or n >= 100:
    print("Ошибка: N должно быть от 1 до 99")
elif n == 1:
    print("Сила = 1")
elif n == 2:
    print("Сила = 2")
elif n == 3:
    print("Сила = 3")
elif n == 4:
    print("Сила = 4")
else:
    тройки = n // 3
    остаток = n % 3
    
    if остаток == 0:
        сила = 3 ** тройки
    elif остаток == 1:
        сила = (3 ** (тройки - 1)) * 4
    else:  # остаток == 2
        сила = (3 ** тройки) * 2
    
    print(f"Сила = {сила}")
